from typing import List, Union

def sum_mixed_list(list: List[Union[int, float]]) -> float:
    """
    Sum a mixed list

    Parameters:
    list(List[Union[int, float]]): First argument

    Return:
    float: a sum of a mixed list
    """
    return sum(list)
